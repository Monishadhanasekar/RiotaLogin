const mongoose = require("mongoose");

const Register = mongoose.Schema({
    fullname: {
        type: String
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    },
    updatedAt: {
        type: Date,
        default: Date.now
    }
})

const registermodel = mongoose.model('register', Register);
module.exports = registermodel;